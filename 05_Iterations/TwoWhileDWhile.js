// While Loop
let index = 0
while (index <= 10) {
    // console.log(index);
    index = index + 2
}

let myArray = ["Batman", "Flash", "Superman"]
let i = 0
while (i < myArray.length) {
    // console.log(`Value in Array at index ${i} : ${myArray[i]}`);
    i++
}

// do While Loop
let score = 1
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);