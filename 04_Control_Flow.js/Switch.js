let month = 2
switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("Febrary");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;
    
    default:
        console.log("Not in Calender");
        break;
}